package core_java_object_oriented;

public class Strings_inbuilt_method_Function {

	public static void main(String[] args) {
		String v1="Arsh";
		System.out.println(v1.toUpperCase());				//ARSH
		System.out.println(v1.toLowerCase());
		System.out.println(v1.contains("Ar"));
		System.out.println(v1.indexOf("r"));
		System.out.println(v1.substring(1));
		System.out.println(v1.substring(0,2));
		System.out.println(v1.charAt(1));
		System.out.println(v1.toCharArray());

		



		
		
	}

}
